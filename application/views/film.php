                charities. ECFS 2015 will be supporting the Scottish Association of Mental Health (SAMH) and Elephant
                Family. It was thinking about our charities, and our vision for the show, that our theme was born.
                We want to make people stop, listen and think. The campaign will be bright, audacious and fun. We want to
                break creative boundaries whilst being accessible to all. We want to remove the social taboo surrounding
                mental healths issues and promote the protection of the endangered Asian elephant. Our campaign this year is
                to ‘Free the Elephant in the Room’.</p>
            <p>So get ready, because ECFS 2015 is going to be an unmissable journey. Sometimes life is tough and just days
                tick on; monotonous, mindless and meaningless. So ECFS hopes to present a year of thought provoking, 'happy
